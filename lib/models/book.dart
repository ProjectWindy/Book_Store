class Book {
  final String id;
  final String title;
  final String author;
  final String authorImg;
  final String cover;
  final String showImage;
  final String age;
  final String genre;
  final String lanugage;
  final double price;
  final double rating;
  final bool seller;
  final String summary;
  final String category;

  Book({
    required this.id,
    required this.title,
    required this.author,
    required this.authorImg,
    required this.cover,
    required this.showImage,
    required this.age,
    required this.genre,
    required this.lanugage,
    required this.price,
    required this.rating,
    required this.seller,
    required this.summary,
    required this.category,
  });
}
 