// Helper function to convert Map to Book object
Book mapToBook(Map<String, dynamic> map) {
  return Book(
    id: map['id'],
    title: map['name'],
    price: double.parse(map['rate']),
    cover: map['image'],
    author: map['author'],
    age: map['age'],
    authorImg: map['author_img'],
    seller: map['seller'],
    genre: map['type'],
    lanugage: map['language'],
    summary: map['description'],
    showImage: map['image'],
    rating: double.parse(map['rating']),
    category: map['type'],
  );
}

// Helper function to get books by type
List<Book> getBooksByType(String type) {
  if (type == 'All') {
    return seedBooks.map((book) => mapToBook(book)).toList();
  }
  return seedBooks
      .where((book) => book['type'] == type)
      .map((book) => mapToBook(book))
      .toList();
}
